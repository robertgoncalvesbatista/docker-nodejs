/**
 * Função de adição de dois valores
 *
 * @param   {number}  a  number parameter
 * @param   {number}  b  number parameter
 *
 * @return  {number}     result of adiction
 */
const adicao = (a, b) => {
  return a + b;
};

/**
 * Função de subtração de dois valores
 *
 * @param   {number}  a  number parameter
 * @param   {number}  b  number parameter
 *
 * @return  {number}     result of subtraction
 */
const subtracao = (a, b) => {
  return a - b;
};

module.exports = { adicao, subtracao };
